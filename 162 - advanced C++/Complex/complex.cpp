//******************************************************************************
#include <iostream>

using namespace std;

#include "complex.h"
//******************************************************************************

Complex::Complex(double r, double i)
{
  real = r;
  imaginary = i;
}


//SET REAL
//==============================================================================
void 
Complex::setReal (double r)
{
  real = r;
}
//==============================================================================


//SET IMAGINARY
//==============================================================================
void 
Complex::setImaginary (double i)
{
  imaginary = i;
}
//==============================================================================


//GET REAL
//==============================================================================
double
Complex::getReal ()
{
  return (real);
}
//==============================================================================


//GET IMAGINARY
//==============================================================================
double
Complex::getImaginary ()
{
  return (imaginary);
}
//==============================================================================


//ADD COMPLEX
//==============================================================================
void
Complex::addComplex (double r, double i)
{
   real += r;
   imaginary += i;
}
//==============================================================================


//SUBTRACT COMPLEX
//==============================================================================
void
Complex::subComplex (double r, double i)
{
  real -= r;
  imaginary -= i;
}
//==============================================================================


//PRINT COMPLEX
//==============================================================================
void
Complex::printComplex()
{
  cout << "(" << real << ", " << imaginary << ")" << endl;
}
//==============================================================================

